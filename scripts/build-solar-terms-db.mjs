// /scripts/build-solar-terms-db.mjs
import fs from "fs";
import path from "path";

const ROOT = process.cwd();

const INPUT_JSON = path.join(ROOT, "data", "solar_terms_1940_2040.json");
const OUTPUT_JS  = path.join(ROOT, "js", "solarTerms.db.js");

const YEAR_MIN = 1940;
const YEAR_MAX = 2040;

const TERM_ORDER_24 = [
  "소한","대한","입춘","우수","경칩","춘분","청명","곡우",
  "입하","소만","망종","하지","소서","대서","입추","처서",
  "백로","추분","한로","상강","입동","소설","대설","동지"
];

const MONTH_BOUNDARY_TERMS_12 = [
  "대설","소한","입춘","경칩","청명","입하","망종","소서","입추","백로","한로","입동"
];

function fail(msg){
  console.error("❌", msg);
  process.exit(1);
}

function readJson(file){
  if(!fs.existsSync(file)) fail(`Input not found: ${file}`);
  const raw = fs.readFileSync(file, "utf-8");
  try { return JSON.parse(raw); }
  catch(e){ fail(`Invalid JSON: ${e.message}`); }
}

function isValidMD(s){
  if(typeof s !== "string") return false;
  if(!/^\d{2}-\d{2}$/.test(s)) return false;
  const [mm, dd] = s.split("-").map(Number);
  if(mm < 1 || mm > 12) return false;
  if(dd < 1 || dd > 31) return false;
  return true;
}

function validateAll(data){
  // 연도 범위
  for(const yy of Object.keys(data)){
    const terms = data[yy];
    if(!terms) fail(`Missing year: ${y} in JSON`);

    // 24개 존재 + 형식 검증
    for(const name of TERM_ORDER_24){
      if(!(name in terms)) fail(`Missing term: ${y} ${name}`);
      const md = terms[name];
      if(!isValidMD(md)) fail(`Bad MM-DD: ${y} ${name} = ${md}`);
    }

    // 정렬 검증 (연중 순서가 뒤집히면 안됨)
    let prev = "";
    for(const name of TERM_ORDER_24){
      const md = terms[name];
      if(prev && md < prev){
        fail(`Not sorted: ${y} ${name}=${md} < prev=${prev}`);
      }
      prev = md;
    }
  }
}

function buildOutputJS(data){
  // keys 정렬
  const years = Object.keys(data).map(Number).sort((a,b)=>a-b);

  // 필요한 연도만 뽑기(1940~2040)
  const filtered = {};
  for(const y of years){
    if(y < YEAR_MIN || y > YEAR_MAX) continue;
    filtered[y] = data[String(y)];
  }

  // JS로 출력(가독성)
  const header = `// /js/solarTerms.db.js
// AUTO-GENERATED by scripts/build-solar-terms-db.mjs
// Source: /data/solar_terms_1940_2040.json
// Format: { [year]: { [termName]: "MM-DD" } }
// Range: ${YEAR_MIN}~${YEAR_MAX}

export const TERM_ORDER_24 = ${JSON.stringify(TERM_ORDER_24, null, 2)};

export const MONTH_BOUNDARY_TERMS_12 = ${JSON.stringify(MONTH_BOUNDARY_TERMS_12, null, 2)};

export const SOLAR_TERMS = ${JSON.stringify(filtered, null, 2)};

// ---- helpers ----
export function getTermMD(year, termName){
  return SOLAR_TERMS?.[year]?.[termName] || "";
}
export function getMD(date){
  const mm = String(date.getMonth()+1).padStart(2,"0");
  const dd = String(date.getDate()).padStart(2,"0");
  return \`\${mm}-\${dd}\`;
}
export function validateSolarTerms(year){
  const t = SOLAR_TERMS[year];
  if(!t) return { ok:false, errors:[\`No SOLAR_TERMS for \${year}\`] };

  const errors = [];
  for(const name of TERM_ORDER_24){
    if(!t[name]) errors.push(\`Missing term: \${year} \${name}\`);
  }

  let prev = "";
  for(const name of TERM_ORDER_24){
    const md = t[name];
    if(md && !/^\\d{2}-\\d{2}$/.test(md)) errors.push(\`Bad format: \${year} \${name}=\${md}\`);
    if(prev && md && md < prev) errors.push(\`Not sorted: \${year} \${name}=\${md} < prev=\${prev}\`);
    if(md) prev = md;
  }

  return { ok: errors.length===0, errors };
}
`;

  return header;
}

function ensureDir(p){
  const dir = path.dirname(p);
  if(!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive:true });
}

(function main(){
  const data = readJson(INPUT_JSON);
  validateAll(data);

  const out = buildOutputJS(data);
  ensureDir(OUTPUT_JS);
  fs.writeFileSync(OUTPUT_JS, out, "utf-8");

  console.log("✅ Built:", OUTPUT_JS);
})();
